public class IfStatements {
    public static void main(String[] args) {
        char grade = 'a';

        if (grade == 'a') {
            System.out.println("Excellent");
        }
        if (grade == 'B') {
            System.out.println("Great Job");
        }
        if (grade == 'c') {
            System.out.println("Not good");
        }
    }
}


